5. Base Legal para el Tratamiento de Datos<br>
La base legal para el tratamiento de datos personales incluye el consentimiento del usuario, la ejecución de un contrato, el cumplimiento de obligaciones legales y el interés legítimo de VisionStock en proporcionar y mejorar sus servicios.<br><br>

6. Compartir Datos Personales <br>
VisionStock no compartirá sus datos personales con terceros, excepto cuando sea necesario para cumplir con obligaciones legales o por requerimiento de autoridades competentes.<br><br>

7. Seguridad de los Datos Personales <br>
Implementamos medidas de seguridad técnicas, administrativas y físicas para proteger los datos personales contra el acceso no autorizado, la divulgación, la alteración o la destrucción. Sin embargo, no podemos garantizar la seguridad absoluta de la información transmitida a través de Internet.<br><br>

8. Derechos de los Titulares de los Datos<br>
Usted tiene derecho a acceder, corregir, actualizar o eliminar sus datos personales en cualquier momento. También puede optar por no recibir comunicaciones comerciales por correo electrónico. Para ejercer estos derechos o para cualquier consulta relacionada con sus datos personales, comuníquese con nosotros utilizando los datos de contacto proporcionados en esta política.<br><br>

9. Periodo de Retención de Datos<br>
Conservamos sus datos personales durante el tiempo necesario para cumplir con los fines descritos en esta política, a menos que la ley exija o permita un período de retención más prolongado.<br><br>

10. Cambios en la Política de Tratamiento de Datos<br>
Nos reservamos el derecho de actualizar y modificar esta política de tratamiento de datos en cualquier momento. Se le notificarán los cambios significativos a través de nuestro sitio web o por otros medios de comunicación. El uso continuado de nuestros servicios después de la publicación de cambios constituye su aceptación de dichos cambios.
Esta política de tratamiento de datos tiene como objetivo garantizar la transparencia y la protección de la privacidad de los datos personales de nuestros usuarios y clientes, sin compartir dichos datos con terceros, excepto en circunstancias específicas y legales. Si tiene alguna pregunta o inquietud sobre esta política, no dude en ponerse en contacto con nosotros.<br><br>

